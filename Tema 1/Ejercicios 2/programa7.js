let num = prompt("Dime un numero del 1-3");

switch(num){
    case 1:
        console.log("Opcion A elegida");
        break;
    case 2:
        console.log("Opcion B elegida");
        break;
    case 3:
        console.log("Opcion C elegida");
        break;
    default:
        console.log("Opcion no valida");
}